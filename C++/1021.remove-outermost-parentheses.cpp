#include <string>
#include <iostream>
#include <vector>
using namespace std;
// @lc code=start
class Solution {
public:
    string removeOuterParentheses(string S) {
        int len = S.size();
        if(len == 0) return S;
        if(len == 2) return string{""};
        // use this pos to record the end of each primitive substring
        vector<int> pos;
        int diff = 0;
        for(int i = 0; i < len; i++){
            if(S[i] == '('){
                diff++;
            }
            if(S[i] == ')'){
                diff--;
            }
            if(diff == 0){
                pos.emplace_back(i);
            }
        }
        S.erase(0,1);
        for(int i = 0; i < pos.size()-1; i++){
            S.erase(pos[i] - 2*i - 1,2);
        }
        S.pop_back();
        return S;
    }
};
